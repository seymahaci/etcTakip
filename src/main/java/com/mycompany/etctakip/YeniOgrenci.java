    private void fetchComboBoxData() {
        String url = "jdbc:mysql://localhost:3306/etc_academy_ybs";
        String username = "root";
        String password = "etc5861";
        String query = "SELECT id, kind, donem, price FROM egitim_etc WHERE aktif = 'aktif'";

        try (Connection connection = DriverManager.getConnection(url, username, password);
             Statement statement = connection.createStatement();
             ResultSet resultSet = statement.executeQuery(query)) {

            // Clear existing items in jComboBox10
            jComboBox10.removeAllItems();

            // Populate jComboBox10 with data from the database
            while (resultSet.next()) {
                String id = resultSet.getString("id");
                String kind = resultSet.getString("kind");
                String donem = resultSet.getString("donem");
                String price = resultSet.getString("price");
                String boxy = kind + " - " + donem + " - (" + price + ")"+ "(id: " + id + ")";

                // ComboBoxItem ile her bir öğeyi oluşturuyoruz
                jComboBox10.addItem(boxy);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

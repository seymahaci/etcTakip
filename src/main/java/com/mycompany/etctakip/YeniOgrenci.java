    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String adi = jTextField5.getText();
        String telefon = jTextField6.getText();
        String taksit = jTextField7.getText();
        String mail = jTextField8.getText();
        String egitim = jComboBox10.getSelectedItem().toString();
        String notlar = jTextArea1.getText();

        Connection connection = null;
        PreparedStatement preparedStatement = null;
        PreparedStatement billingStatement = null;
        PreparedStatement odemeEgitimStatement = null;

        try {


            connection = DriverManager.getConnection(url, username, password);
            connection.setAutoCommit(false);

            // Insert into ogrenci_etc table
            String insertOgrenciQuery = "INSERT INTO ogrenci_etc (adi, telefon, mail, text) VALUES (?, ?, ?, ?)";
            preparedStatement = connection.prepareStatement(insertOgrenciQuery, Statement.RETURN_GENERATED_KEYS);
            preparedStatement.setString(1, adi);
            preparedStatement.setString(2, telefon);
            preparedStatement.setString(3, mail);
            preparedStatement.setString(4, notlar);
            preparedStatement.executeUpdate();

            // Get the auto-generated id for the inserted row in ogrenci_etc
            ResultSet generatedKeys = preparedStatement.getGeneratedKeys();
            int ogrenciID = -1;
            if (generatedKeys.next()) {
                ogrenciID = generatedKeys.getInt(1);
            }
            generatedKeys.close();

            // Insert into egitim_ogrenci_id table
            String insertEgitimOgrenciQuery = "INSERT INTO egitim_ogrenci_id (course_id, ogrenci_id) VALUES (?, ?)";
            odemeEgitimStatement = connection.prepareStatement(insertEgitimOgrenciQuery);
            odemeEgitimStatement.setInt(1, selectedId);
            odemeEgitimStatement.setInt(2, ogrenciID);
            odemeEgitimStatement.executeUpdate();
            
            double originalPrice = getPrice(selectedId);
            if(jToggleButton1.isSelected() && !jTextField1.getText().equals("")){
                double discount = Double.parseDouble(jTextField1.getText());
                originalPrice = originalPrice * (1 - discount / 100);
                //jTextField2.setText(String.valueOf(originalPrice));
            }else if(jToggleButton1.isSelected() && jTextField1.getText().equals("") && !jTextField2.getText().equals("")){
                originalPrice = Double.parseDouble(jTextField2.getText());
            }else if(jToggleButton1.isSelected()){
                JOptionPane.showMessageDialog(null, "İndirim yahut yeni ücret belirt.");
            }
            
            if (jRadioButton1.isSelected()) {
                billingStatement = connection.prepareStatement("INSERT INTO billing (date, type_in_out, amount, tick) VALUES (CURDATE(), 'gelir_eg', ?, 'ödenmedi')", Statement.RETURN_GENERATED_KEYS);
                billingStatement.setDouble(1, originalPrice);
                billingStatement.executeUpdate();

                // Get the auto-generated id for the inserted row in billing table
                ResultSet generatedBillingKeys = billingStatement.getGeneratedKeys();
                int billingId = -1;
                if (generatedBillingKeys.next()) {
                    billingId = generatedBillingKeys.getInt(1);
                }
                generatedBillingKeys.close();

                // Insert into odeme_egitim_id table
                String odemeEgitimInsertQuery = "INSERT INTO odeme_egitim_id (odeme_id, taraf_id, egitim_id) VALUES (?, ?, ?)";
                odemeEgitimStatement = connection.prepareStatement(odemeEgitimInsertQuery);
                odemeEgitimStatement.setInt(1, billingId);
                odemeEgitimStatement.setInt(2, ogrenciID);
                odemeEgitimStatement.setInt(3, selectedId);
                odemeEgitimStatement.executeUpdate();
                
                
            }else  if (jRadioButton2.isSelected()) {
                int taksitint = Integer.parseInt(taksit);
                double amountPerInstallment = originalPrice/taksitint;
                LocalDate currentDate = LocalDate.now();

                for (int i = 0; i < taksitint; i++) {
                    billingStatement = connection.prepareStatement("INSERT INTO billing (date, type_in_out, amount, tick) VALUES (?, 'gelir_eg', ?, 'ödenmedi')", Statement.RETURN_GENERATED_KEYS);
                    billingStatement.setDate(1, java.sql.Date.valueOf(currentDate.plusMonths(i)));
                    billingStatement.setDouble(2, amountPerInstallment);
                    billingStatement.executeUpdate();

                    // Get the auto-generated id for the inserted row in billing table
                    ResultSet generatedBillingKeys = billingStatement.getGeneratedKeys();
                    int billingId = -1;
                    if (generatedBillingKeys.next()) {
                        billingId = generatedBillingKeys.getInt(1);
                    }
                    generatedBillingKeys.close();

                    // Insert into odeme_egitim_id table
                    String odemeEgitimInsertQuery = "INSERT INTO odeme_egitim_id (odeme_id, taraf_id, egitim_id) VALUES (?, ?, ?)";
                    odemeEgitimStatement = connection.prepareStatement(odemeEgitimInsertQuery);
                    odemeEgitimStatement.setInt(1, billingId);
                    odemeEgitimStatement.setInt(2, ogrenciID);
                    odemeEgitimStatement.setInt(3, selectedId);
                    odemeEgitimStatement.executeUpdate();
                }
            }
            
            
            connection.commit();
            this.dispose();

            System.out.println("Values inserted successfully!");
        } catch (SQLException | NumberFormatException e) {
            try {
                if (connection != null) {
                    connection.rollback();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
            e.printStackTrace();
        } finally {
            try {
                if (preparedStatement != null) {
                    preparedStatement.close();
                }
                if (odemeEgitimStatement != null) {
                    odemeEgitimStatement.close();
                }
                if (billingStatement != null) {
                    billingStatement.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } 
    }//GEN-LAST:event_jButton1ActionPerformed
    

